CREATE TABLE guesses (
  guess_id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
	user_id INTEGER
    REFERENCES users(user_id) ON DELETE CASCADE NOT NULL
	week_id INTEGER
    REFERENCES weeks(week_id) ON DELETE CASCADE NOT NULL,
	guess_1 TINYINT NOT NULL,
	guess_2 TINYINT NOT NULL,
	guess_3 TINYINT NOT NULL,
	guess_4 TINYINT NOT NULL,
	guess_5 TINYINT NOT NULL,
	power_ball TINYINT NOT NULL,
  date_created TIMESTAMP DEFAULT now() NOT NULL,
  message VARCHAR(280) NOT NULL,
	has_won BOOLEAN NOT NULL DEFAULT FALSE
);